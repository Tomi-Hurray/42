#include "libft.h"

int	ft_isalnum(char estr)
{
	if (((estr - 48) >= 0 && (estr - 48) <= 9) \
|| ((estr <= 122 && estr >= 97) || (estr >= 65 && estr <= 90)))
		return (1);
	else
		return (0);
}
